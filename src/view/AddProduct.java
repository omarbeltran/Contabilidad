    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        id = txtId.getText();
        name = txtName.getText();
        if(Validation.validateData(name, id))
        {
            if(controllerProducto.agregarProducto(id, txtValor.getText(), name))
            {
                JOptionPane.showMessageDialog(null, "Producto agregado correctamente");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "El id ya existe, no se puedo agregar el producto");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Datos no v√°lidos, verifique ID y Nombre");
        }
    }//GEN-LAST:event_btnAddActionPerformed


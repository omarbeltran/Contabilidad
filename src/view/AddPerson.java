    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        id = txtId.getText();
        nombre = txtName.getText();
        //tipoRegimen no se verifica pese a enviarse por parametro, puesto que esta definido
        //en la clase Regimen
        if(Validation.verifyDataPerson(nombre, id, tipoRegimen))
        {
            if(controllerPersona.addPersona(id, txtName.getText(), tipoRegimen))
            {
                JOptionPane.showMessageDialog(null, "Persona agregada correctamente");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "El id ya existe, no se puedo agregar la persona");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Datos no v√°lidos, verifique ID y Nombre");
        }
    }//GEN-LAST:event_btnAddActionPerformed

